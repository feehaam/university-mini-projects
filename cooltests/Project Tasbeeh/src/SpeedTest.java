      private void startActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startActionPerformed
            if(target.getText().toString().length()<1)
                  JOptionPane.showMessageDialog(this, "Please set the target first!");
            else{
            plus1.setEnabled(true);
            plus2.setEnabled(true);
            plus3.setEnabled(true);
            plus5.setEnabled(true);
            plus8.setEnabled(true);
            plus10.setEnabled(true);
            plus100.setEnabled(true);
            plus1000.setEnabled(true);
            count.setText("0");
            start.setText("Started!");
            title.setText("Target is: ");
            start.setEnabled(false);
            target.setEditable(false);
            target.setBackground(Color.gray);
            timea=System.currentTimeMillis();
            }
      }//GEN-LAST:event_startActionPerformed

