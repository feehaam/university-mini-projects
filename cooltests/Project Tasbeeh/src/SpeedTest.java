      private void countPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_countPropertyChange
            if(start.getText().toString().equals("Started!")&&count.getText().toString().length()<9){
                  if(Integer.parseInt(count.getText().toString())>=Integer.parseInt(target.getText().toString())){
                        timeb=System.currentTimeMillis();
                        JOptionPane.showMessageDialog(this, "Done");  
                        long time=timeb-timea;
                        long sec=time/1000;
                        long min=sec/60;
                        sec = sec-min*60;
                        String s=Long.toString(min)+" : "+Long.toString(sec)+" min!";
                        count.setText(s);
                        title.setText("Please set the target number :");
                        target.setText("");
                        start.setText("Start");
                        start.setEnabled(true);
                        
            plus1.setEnabled(false);
            plus2.setEnabled(false);
            plus3.setEnabled(false);
            plus5.setEnabled(false);
            plus8.setEnabled(false);
            plus10.setEnabled(false);
            plus100.setEnabled(false);
            plus1000.setEnabled(false);
            
            tar();
                  }
            }
      }//GEN-LAST:event_countPropertyChange

